    public void eliminarRegistroTabla(){
        DefaultTableModel tb = (DefaultTableModel) tablaSubCategoria.getModel();
        int a = tablaSubCategoria.getRowCount()-1;
        for (int i = a; i >= 0; i--) {           
        tb.removeRow(tb.getRowCount()-1);
        } 
        eliminarRegistroTablaProducto();
    }
    
